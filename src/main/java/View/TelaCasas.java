    protected void mostrarTabela(){
        javax.swing.table.DefaultTableModel tabela = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        tabela.setRowCount(0);
        
        ResidenciaController r = new ResidenciaController();
        ArrayList<Residencia> casas = r.exibir();
        for(Residencia a : casas){
            tabela.addRow(new Object[]{a.getNumero(), a.getRua(), a.getCep(), a.getDono()});
        }
    }

