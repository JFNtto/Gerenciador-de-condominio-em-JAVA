    private void btnEdicaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdicaoActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = jTable1.getSelectedRow();
        
        if (linhaSelecionada >= 0){
            int numero_casa = (int)jTable1.getValueAt(linhaSelecionada, 0);
            String rua = (String)jTable1.getValueAt(linhaSelecionada, 1);
            String cep = (String)jTable1.getValueAt(linhaSelecionada, 2);
            int dono_id = (int)jTable1.getValueAt(linhaSelecionada, 3);
            
            TelaEditarCasa t = new TelaEditarCasa(numero_casa, rua, cep, dono_id);
            t.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Escolha algum item da tabela para poder editar!");
        }
    }//GEN-LAST:event_btnEdicaoActionPerformed

