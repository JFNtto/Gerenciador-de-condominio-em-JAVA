    private void bntRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntRemoveActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = jTable1.getSelectedRow();
        
        if (linhaSelecionada >= 0){
            int numero_casa = (int)jTable1.getValueAt(linhaSelecionada, 0);
            
            ResidenciaController r = new ResidenciaController();
            if(r.apagaCasa(numero_casa)){
                JOptionPane.showMessageDialog(null, "Item apagado da tabela");
                
                
                this.dispose();
                TelaCasas t = new TelaCasas();
                t.setVisible(true);               
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao apagar. Existem moradores l√° ou pagamentos ativos", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Escolha algum item da tabela para poder apagar!");
        }
    }//GEN-LAST:event_bntRemoveActionPerformed

